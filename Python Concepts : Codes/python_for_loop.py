# For loop

for i in range(1,11):
    print(i)

# For loop with custom step value
for i  in range(1,11,2):
    print(i)

for i in range(10,0,-1):
    print(i)


# Print the sum of numbers in array
int_list = [4,8,-2,10,5]
sum =0 
for i in int_list:
    sum = sum + i
print(sum)

